const container = document.querySelector("#container");
const nameBtn = document.querySelector("#nameBtn");
const priceBtn = document.querySelector("#priceBtn");
const themeBtn = document.querySelector("#themeBtn");

const renderArticles = (articles) => {
  //Para evitar repeticiones en los renderizados, vaciamos el ul
  container.innerHTML = "";
  for (const element of articles) {
    const li = document.createElement("li");
    li.innerHTML = `
          <img src="${element.imageUrl}" alt="${element.name}" />
          <h2>${element.name}</h2>
          <h3>${element.brand}</h3>
          <p>${element.price}€</p>
        `;
    container.appendChild(li);
  }
};

//Añadir eventos a los botones
nameBtn.addEventListener("click", () => {
  consolas.sort((a, b) => a.name.localeCompare(b.name));
  renderArticles(consolas)
});

priceBtn.addEventListener("click", () => {
  consolas.sort((a, b) => a.price - b.price);
  renderArticles(consolas)
});

themeBtn.addEventListener("click", () => {
  document.body.classList.toggle("dark")
})

//Lanzamos la función para que al abrir la aplicación se pinten los articulos automáticamente
renderArticles(consolas);
